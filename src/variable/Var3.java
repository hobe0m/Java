package variable;

public class Var3 {
    public static void main(String[] args) {
        int a; // 변수 선언(정수 데이터를 담을 수 있는 공간 창출)
        a = 10; // 변수 초기화 a(10)
        System.out.println(a);
        
        a = 50; // 변수의 값 변경
        System.out.println(a);

        // 변수의 값을 변경하면, 기존의 값은 사라진다.
        // 변수의 선언이란 데이터를 저장할 수 있는 컴퓨터의 메모리 공간을 확보 후 이름을 지정하여 접근할 수 있게 하는 것 
    }
}
