package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수 a(정수) 선언
        a = 10; // 변수 초기화, a에다 10이라는 숫자를 담음

        System.out.println(a);

        a = 20; // a에 20을 담음, 변수기에 가능(변할 수 있다)
        System.out.println(a);

        a = 30; // a에 30을 담음
        System.out.println(a);

    }
}
