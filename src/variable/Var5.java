package variable;

public class Var5 {
    public static void main(String[] args) {

        // 처음으로 변수에 값을 부여하는 것 = 초기화

        int a; // 변수 선언
        a = 1; // 변수 초기화(제일 처음에 값을 부여한 것이기 때문)
        System.out.println(a);

        int b = 2; // 변수의 선언과 초기화를 한번에 할 수도 있다.
        System.out.println(b);

        int c = 3, d = 4; // 여러 변수의 선언과 초기화도 한번에 할 수 있다.
        System.out.println(c);
        System.out.println(d);

        // 변수는 * 무조건 * 초기화를 해야한다.

    }
}
